import csv
import os
import ipywidgets as widgets
from IPython.display import display, clear_output
from google.colab import files



def guardarArchivo(nombreArchivo, listaDatos, campos):
    with open(nombreArchivo, mode='w', newline='', encoding='utf-8') as f:
        escritor = csv.DictWriter(f, fieldnames=campos)
        escritor.writeheader()
        escritor.writerows(listaDatos)

def cargarArchivo(nombreArchivo):
    if not os.path.exists(nombreArchivo): return []
    with open(nombreArchivo, mode='r', encoding='utf-8') as f:
        return list(csv.DictReader(f))



class LogicaIFN:
    def __init__(self):
        self.archivoB = "brigadas.csv"
        self.archivoC = "conglomerados.csv"
        self.archivoFlora = "flora.csv"
        self.archivoFauna = "fauna.csv"

        self.camposB = ["idBrigada", "liderProfesional", "coinvestigadorLocal"]
        self.camposC = ["idConglo", "coordenadasGPS", "idBrigada"]
        self.camposFl = ["idSp", "idConglo", "nombreComun", "uso", "dap", "categoria"]
        self.camposFa = ["idSp", "idConglo", "especie", "grupo", "estadoConservacion"]

        self.listaB = cargarArchivo(self.archivoB)
        self.listaC = cargarArchivo(self.archivoC)
        self.listaFl = cargarArchivo(self.archivoFlora)
        self.listaFa = cargarArchivo(self.archivoFauna)

    def clasificarDiametro(self, valorDap):
        d = float(valorDap)

        if 2.5 <= d <= 9.9: return "Brinzal"
        elif 10.0 <= d <= 29.9: return "Latizal"
        elif d >= 30.0: return "Fustal"

        return "No definido"

    def agregarBrigada(self, idB, lider, local):
        self.listaB.append({"idBrigada": idB, "liderProfesional": lider, "coinvestigadorLocal": local})
        guardarArchivo(self.archivoB, self.listaB, self.camposB)

    def agregarConglomerado(self, idC, gps, idB):
        self.listaC.append({"idConglo": idC, "coordenadasGPS": gps, "idBrigada": idB})
        guardarArchivo(self.archivoC, self.listaC, self.camposC)

    def agregarFlora(self, idS, idC, nom, uso, dap):
        cat = self.clasificarDiametro(dap)

        self.listaFl.append({
            "idSp": idS,
            "idConglo": idC,
            "nombreComun": nom,
            "uso": uso,
            "dap": dap,
            "categoria": cat
        })

        guardarArchivo(self.archivoFlora, self.listaFl, self.camposFl)

    def agregarFauna(self, idS, idC, esp, grp, est):
        self.listaFa.append({
            "idSp": idS,
            "idConglo": idC,
            "especie": esp,
            "grupo": grp,
            "estadoConservacion": est
        })

        guardarArchivo(self.archivoFauna, self.listaFa, self.camposFa)


