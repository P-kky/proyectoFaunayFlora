# EDITAR
# =========================

def alEditarB(b):

    for brigada in logica.listaB:

        if brigada['idBrigada'] == txtIdB.value:

            brigada['liderProfesional'] = txtLider.value
            brigada['coinvestigadorLocal'] = txtLocal.value

    guardarArchivo(logica.archivoB, logica.listaB, logica.camposB)

    with salida:
        clear_output()
        print(f"Brigada {txtIdB.value} editada.")


def alEditarC(b):

    for c in logica.listaC:

        if c['idConglo'] == txtIdC.value:

            c['coordenadasGPS'] = txtGPS.value
            c['idBrigada'] = drpB.value

    guardarArchivo(logica.archivoC, logica.listaC, logica.camposC)

    with salida:
        clear_output()
        print(f"Conglomerado {txtIdC.value} editado.")


def alEditarFl(b):

    for f in logica.listaFl:

        if f['idSp'] == txtIdFl.value:

            f['nombreComun'] = txtNomFl.value
            f['uso'] = drpUsoFl.value
            f['dap'] = numDap.value
            f['categoria'] = logica.clasificarDiametro(numDap.value)

    guardarArchivo(logica.archivoFlora, logica.listaFl, logica.camposFl)

    with salida:
        clear_output()
        print(f"Flora {txtIdFl.value} editada.")


def alEditarFa(b):

    for a in logica.listaFa:

        if a['idSp'] == txtIdFa.value:

            a['especie'] = txtEspFa.value
            a['grupo'] = drpGrpFa.value
            a['estadoConservacion'] = drpEstFa.value

    guardarArchivo(logica.archivoFauna, logica.listaFa, logica.camposFa)

    with salida:
        clear_output()
        print(f"Fauna {txtIdFa.value} editada.")

